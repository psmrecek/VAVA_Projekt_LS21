    private void showInfo(){
        winnerLabel.setText(match.getWinner());
        resultLabel.setText(match.getResult());
        typeLabel.setText(match.getType());
        timeLabel.setText(InputProcessor.dateToString(match.getDate()));
        
        teamsList.setListData(getTeams());
        
        logger.info("Match info displayed");
        this.setVisible(true);
    }
    
    public String[] getTeams(){
        String [] teams = new String[this.match.getTeamList().size()];
        
        for(int item = 0; item < this.match.getTeamList().size(); item++)
            teams[item] = this.match.getTeamList().get(item).getName();
        return teams;
    }
    
